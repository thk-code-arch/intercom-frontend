<template>
    <!-- Chat content -->
    <div class="flex-1 flex flex-col max-h-full max-w-full bg-white overflow-hidden">
        <!-- Top bar -->
        <div class="border-b flex px-6 py-2 items-center flex-none">
            <div class="flex flex-col">
                <h3 class="text-grey-darkest mb-1 font-extrabold">#ChatRoomName</h3>
                <div class="text-grey-dark text-sm truncate">
                  Project Description
                </div>
            </div>
        </div>
        <!-- Chat messages -->
        <!-- Chat messages -->
        <div class="px-6 py-4 flex-1 overflow-y-scroll" v-chat-scroll>
            <!-- A message -->
           <div class="hist_messages" v-for="(hist_msg, hist_index) in messages" :key="hist_index">

            <div class="flex items-start mb-4 text-sm" v-if="hist_msg.user">
                <img :src="$profile_image+hist_msg.user.profile_image" class="w-10 h-10 rounded mr-3">
                <div class="flex-1 overflow-hidden">
                    <div>
                        <span class="font-bold">{{ hist_msg.user.username }}</span>
                        <span class="text-grey text-xs"> {{ hist_msg.time }}</span>
                    </div>
                    <p class="text-black leading-normal">{{ hist_msg.message }}</p>
                </div>
            </div>
            </div>
            <!-- A message -->
        </div>
        <!-- New message -->
        <div class="pb-6 px-4 flex-none">
            <div class="flex rounded-lg border-2 border-grey overflow-hidden">
                <span class="text-3xl text-grey border-r-2 border-grey p-2">
                    <svg class="fill-current h-6 w-6 block" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20"><path d="M16 10c0 .553-.048 1-.601 1H11v4.399c0 .552-.447.601-1 .601-.553 0-1-.049-1-.601V11H4.601C4.049 11 4 10.553 4 10c0-.553.049-1 .601-1H9V4.601C9 4.048 9.447 4 10 4c.553 0 1 .048 1 .601V9h4.399c.553 0 .601.447.601 1z"/></svg>
                  </span>
          <form @submit.prevent="clickButton()">
                <input v-model="message" type="text"  class="w-full px-4" placeholder="Message #general" />
                <button type="submit" :disabled="message.length == 0"></button>
          </form>
            </div>
        </div>
    </div>
</template>

<script>


export default {
  name: "ChatWindow",
    data() {
        return {
            message: '',
        }
    },
  computed: {
    messages() {
    return this.$store.state.chatroom.messages
  },
  },
  methods: {
    clickButton() {
      // this.$socket.client is `socket.io-client` instance
      this.$store.dispatch('chatroom/CHAT_sendmessage',this.message);
      this.message = '';
    }
  },
  mounted(){
      this.$store.dispatch('iosockets/init_chatroom');
      this.$store.dispatch('chatroom/load_chatlog');
  }
};
</script>
